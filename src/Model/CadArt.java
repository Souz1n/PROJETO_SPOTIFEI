package Model;

public class CadArt {
    
    String nome, banda;

    public CadArt() {
    }

    public CadArt(String nome, String banda) {
        this.nome = nome;
        this.banda = banda;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getBanda() {
        return banda;
    }

    public void setBanda(String banda) {
        this.banda = banda;
    }
    
    
    
}
